public class app1JavaIntOperations {

    public static void main(String [] args){
        // Iremos exbir algumas operações com inteiros. Mas primeiro iremos simplesmente exibir um inteiro.
        System.out.println("Esse é o inteiro " + (7));
        // Algumas operações básicas com inteiros.
        System.out.println("A soma de 7 + 3 é " + (7 + 3));
        System.out.println("A subtração de 7 - 3 é " + (7 - 3));
        System.out.println("O produto de 7 * 3 é " + (7 * 3));
        System.out.println("A divisão de 7 / 3 é " + (7 / 3));
        System.out.println("O resto da divisão de 7 % 3 é " + (7 % 3));
    }
}
